package JavaRecap.OOP;

public class Porsche {

    String brand="Porshe";
    int door=2;
    int windows=4;
    String color="Blue";
    boolean carFax=true;
    double price=124.5;

    public String getBrand() {
        return brand;
    }

    public void setBrand(String brand) {
        this.brand = brand;
    }

    public int getDoor() {
        return door;
    }

    public void setDoor(int door) {
        this.door = door;
    }

    public int getWindows() {
        return windows;
    }

    public void setWindows(int windows) {
        this.windows = windows;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public boolean isCarFax() {
        return carFax;
    }

    public void setCarFax(boolean carFax) {
        this.carFax = carFax;
    }

    public double getPrice() {
        return price;
    }

    public void setPrice(double price) {
        this.price = price;
    }



}
