package OOP_Practice.inheritance.inheritance2;

public class Flower {

    String color;
    String name;
    double price;
    int size;

   public Flower(){}
    public Flower(String color, String name, double price, int size) {
        this.color = color;
        this.name = name;
        this.price = price;
        this.size = size;
    }
}
